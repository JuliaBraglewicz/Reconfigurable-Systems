module verify(
    input [31:0]Y,
    input [31:0]Z
    );
    
initial
begin
    #3 if(Y!=32'b11111111111111111011100111101011 || Z!=32'b00000000000000010000001110000101) $stop;
    #2 if(Y!=32'b0 || Z!=32'b0) $stop;
    #2 if(Y!=32'b11111111110011000100110100100101 || Z!=32'b00000110001100001100101110000101) $stop;
    #2 if(Y!=32'b00000000001100111011011001100000 || Z!=32'b11111001110011101101000000000000) $stop;
    #2 if(Y!=32'b11111111101001010010100001100000 || Z!=32'b00000001100001011010111101001000) $stop;
    #2 if(Y!=32'b11111111111111111011001011100001 || Z!=32'b00000000000000011100110001111011) $stop;
    #2 if(Y!=32'b00000100100110000010000010101110 || Z!=32'b11101001101000111111001001001000) $stop;
    #2 if(Y!=32'b00000010011001000101111001100000 || Z!=32'b11110001101100101010100000000000) $stop;
end
 
endmodule
